let currentLang = 'zh';

function switchLanguage(lang) {
    currentLang = lang;
    updateTranslations();
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
    document.getElementById('lang-toggle').textContent = lang === 'zh' ? 'EN/中' : '中/EN';
}

function updateTranslations() {
    const elements = document.querySelectorAll('[id]');
    elements.forEach(element => {
        const key = element.id;
        if (translations[currentLang][key]) {
            const value = translations[currentLang][key];
            
            if (typeof value === 'object') {
                if (element.classList.contains('timeline-item')) {
                    const yearElement = element.querySelector('.timeline-year');
                    const eventElement = element.querySelector('.timeline-event');
                    
                    if (yearElement) yearElement.textContent = value.year;
                    if (eventElement) eventElement.textContent = value.event;
                }
            } else {
                if (element.tagName !== 'IMG' && element.tagName !== 'BUTTON') {
                    element.textContent = value;
                }
            }
        }
    });
    
    document.title = translations[currentLang]['page-title'];
}

document.addEventListener('DOMContentLoaded', function() {
    document.documentElement.lang = 'zh-CN';
    
    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', function() {
            const newLang = currentLang === 'zh' ? 'en' : 'zh';
            switchLanguage(newLang);
        });
    }
    
    updateTranslations();
});